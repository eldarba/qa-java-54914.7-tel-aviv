package b;

public class Demo3 {

	public static void main(String[] args) {
		
		int a = 8;
		int b = 78;
		int sum = a + b;
		
		System.out.print(a);
		System.out.print(" + ");
		System.out.print(b);
		System.out.print(" = ");
		System.out.println(sum);
		
		// string concatenation - create one unit of text (string) from many values
		
		String message = a + " + " + b + " = " + sum;
		System.out.println(message);

	}

}
